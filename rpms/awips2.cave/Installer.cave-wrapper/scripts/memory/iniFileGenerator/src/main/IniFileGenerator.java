    @XmlAccessorType(XmlAccessType.NONE)
    private static class SubstitutionValue {

        @XmlElement
        private String value;

        private SubstitutionValue() {
        }

        public String getValue() {
            return value;
        }

        public void setValue(String value) {
            this.value = value;
        }
    }
