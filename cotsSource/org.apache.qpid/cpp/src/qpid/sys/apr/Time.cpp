#include "qpid/sys/Time.h"

#include <apr_time.h>

namespace qpid {
namespace sys {
	
AbsTime AbsTime::now() {
    AbsTime time_now;
    time_now.time_ns = apr_time_now() * TIME_USEC;
    return time_now;
}

}}

