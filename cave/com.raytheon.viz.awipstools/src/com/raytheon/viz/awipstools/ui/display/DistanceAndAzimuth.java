public class DistanceAndAzimuth {
    private double distance;

    private double azimuth;

    private Coordinate coordinate;

    private double[] screenXY;

    public DistanceAndAzimuth() {

    }

    public double getDistance() {
        return distance;
    }

    public void setDistance(double distance) {
        this.distance = distance;
    }

    public double getAzimuth() {
        return azimuth;
    }

    public void setAzimuth(double azimuth) {
        this.azimuth = azimuth;
    }

    public Coordinate getCoordinate() {
        return coordinate;
    }

    public void setCoordinate(Coordinate coordinate) {
        this.coordinate = coordinate;
    }

    public double[] getScreenXY() {
        return screenXY;
    }

    public void setScreenXY(double[] screenXY) {
        this.screenXY = screenXY;
    }

    public Coordinate getReverseCoordinate() {
        return new Coordinate(getScreenXY()[0], getScreenXY()[1]);
    }

    public String toString() {
        return "Az: " + this.azimuth + "; Dist: " + this.distance + ";";
    }
}
