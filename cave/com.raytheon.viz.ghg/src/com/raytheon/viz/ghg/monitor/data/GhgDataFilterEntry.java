package com.raytheon.viz.ghg.monitor.data;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;

/**
 * A serialization bean for storing Map entries for GHG configuration.
 * 
 * @author wdougherty
 * 
 */
@XmlAccessorType(XmlAccessType.NONE)
public class GhgDataFilterEntry {

    @XmlElement
    private String key;

    @XmlElement
    private GhgDataFilter value;

    /**
     * @return the key
     */
    public String getKey() {
        return key;
    }

    /**
     * @param key
     *            the key to set
     */
    public void setKey(String key) {
        this.key = key;
    }

    /**
     * @return the value
     */
    public GhgDataFilter getValue() {
        return value;
    }

    /**
     * @param value
     *            the value to set
     */
    public void setValue(GhgDataFilter value) {
        this.value = value;
    }
}