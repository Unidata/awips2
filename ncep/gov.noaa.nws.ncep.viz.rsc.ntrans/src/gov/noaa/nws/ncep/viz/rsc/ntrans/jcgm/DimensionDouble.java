package gov.noaa.nws.ncep.viz.rsc.ntrans.jcgm;

import java.awt.geom.Dimension2D;

/**
 * A {@link Dimension2D} implementation that keeps the width and height in
 * double precision.
 *
 * @author Philippe Cad√©
 * @version $Id$
 */
public class DimensionDouble extends Dimension2D {

	private double width;
	private double height;

	DimensionDouble(double width, double height) {
		this.width = width;
		this.height = height;
	}

	@Override
	public double getHeight() {
		return this.height;
	}

	@Override
	public double getWidth() {
		return this.width;
	}

	@Override
	public void setSize(double width, double height) {
		this.width = width;
		this.height = height;
	}

}